    private void removeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeButtonActionPerformed
        if (surgeryComboBox.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "No surgery to remove.\n",
                    "Error ",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }

        String srg = surgeryComboBox.getSelectedItem().toString();

        Object[] o = {"Remove", "Cancel"};

        int option = JOptionPane.showOptionDialog(this, "You are about to delete the following surgery:\n"
                + srg
                + "\n\n Are you sure you want to remove it data permanently form the database?",
                "Warning! Data removing",
                JOptionPane.OK_CANCEL_OPTION,
                JOptionPane.WARNING_MESSAGE, null, o, o[1]);

        if (option == 0) {
            //remove data
            if (!SurgeriesListPanel_EN.removeSurgeryEntry(srg)) {
                JOptionPane.showConfirmDialog(null,
                        "Surgery could not be deleted. \n"
                        + "There might be patients that are scheduled or need to be \n"
                        + "scheduled for this surgery.",
                        "Error deleting surgery",
                        JOptionPane.DEFAULT_OPTION);
            }
            this.dispose();
        }
    }//GEN-LAST:event_removeButtonActionPerformed

    private void surgeryComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_surgeryComboBoxActionPerformed
        if (surgeryComboBox.getSelectedItem() == null) {
            pathologyTextField.setText("");
            avgDurationFormattedTextField.setText("");
            stdDeviationFormattedTextField.setText("");
            return;
        }

        int selectedSurgeryIndex = surgeryComboBox.getSelectedIndex();
        if (selectedSurgeryIndex == -1) {
            //nothing selected. Make sure everything is empty
            pathologyTextField.setText("");
            avgDurationFormattedTextField.setText("");
            stdDeviationFormattedTextField.setText("");
            return;
        }

        //update fields
        pathologyTextField.setText(surgeriesDetails.get(selectedSurgeryIndex).get(1));
        avgDurationFormattedTextField.setText(surgeriesDetails.get(selectedSurgeryIndex).get(2));
        stdDeviationFormattedTextField.setText(surgeriesDetails.get(selectedSurgeryIndex).get(3));
    }//GEN-LAST:event_surgeryComboBoxActionPerformed

