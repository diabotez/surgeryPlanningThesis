    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        if (surgeryComboBox.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "No surgery to update. Please select one.\n",
                    "Error ",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }

        String srg = surgeryComboBox.getSelectedItem().toString();
        String ptg = pathologyTextField.getText();
        String avg = avgDurationFormattedTextField.getText();
        String std = stdDeviationFormattedTextField.getText();

        if ("".equals(srg) || "".equals(ptg) || "".equals(avg) || "".equals(std)) {
            JOptionPane.showMessageDialog(this, "One ore more fields are not completed.\n", "Error ", JOptionPane.ERROR_MESSAGE);
            return;
        }
        Object[] o = {"Update", "Cancel"};

        int option = JOptionPane.showOptionDialog(this, "The datas you introduced are:\n"
                + "\nsurgery - " + srg
                + "\npathology - " + ptg
                + "\navrage duration - " + avg
                + "\nstandard deviation - " + std
                + "\n\n Are you sure you want to update the old data with these ones?",
                "Check info",
                JOptionPane.OK_CANCEL_OPTION,
                JOptionPane.QUESTION_MESSAGE, null, o, o[0]);

        if (option == 0) {
            //update the data
            SurgeriesListPanel_EN.updateSurgeryEntry(srg, ptg, avg, std);
            this.dispose();
        }
    }//GEN-LAST:event_updateButtonActionPerformed

